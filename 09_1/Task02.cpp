// Черепашка. На квадратной доске расставлены целые неотрицательные числа, каждое из которых
// не превосходит 100. Черепашка, находящаяся в левом нижнем углу, мечтает попасть в правый
// верхний. При этом она может переползать только в клетку справа или сверху и хочет, чтобы
// сумма всех чисел, оказавшихся у нее на пути, была бы минимальной. Определить эту сумму. Ввод и
// вывод организовать при помощи текстовых файлов. Формат входных данных: в первой строке входного
// файла записано число N - размер доски (1 < N < 80). Далее следует N строк, каждая из которых
// содержит N целых чисел, представляющих доску. В выходной файл нужно вывести единственное число:
// минимальную сумму.

#include <fstream>
#include <iostream>
#include <limits>
#include <vector>

using namespace std;

int main() {
    ifstream file1("a.txt");
    // FIX_ME: не проверялось открытие входного файла — программа молча работала с пустым потоком.
    // ifstream file1("a.txt");
    if (!file1.is_open()) {
        cerr << "Ошибка: не удалось открыть a.txt\n";
        return 1;
    }

    ofstream file2("b.txt");
    // FIX_ME: не проверялось открытие выходного файла.
    // ofstream file2("b.txt");
    if (!file2.is_open()) {
        cerr << "Ошибка: не удалось создать b.txt\n";
        return 1;
    }

    int N;
    file1 >> N;

    // FIX_ME: при неверном N возможен некорректный размер вектора — добавлена проверка диапазона.
    // file1 >> N;
    if (N <= 1 || N >= 80) {
        cerr << "Ошибка: N должен удовлетворять 1 < N < 80\n";
        return 1;
    }

    vector<vector<int> > b(N, vector<int>(N));

    for (int i = 0; i < N; i++) {
        for (int j = 0; j < N; j++) {
            file1 >> b[i][j];
        }
    }
    file1.close();

    vector<vector<int> > dp(N, vector<int>(N, numeric_limits<int>::max()));

    dp[N - 1][0] = b[N - 1][0];

    for (int i = N - 1; i >= 0; i--) {
        for (int j = 0; j < N; j++) {
            if (i < N - 1) {
                dp[i][j] = min(dp[i][j], dp[i + 1][j] + b[i][j]);
            }
            if (j > 0) {
                dp[i][j] = min(dp[i][j], dp[i][j - 1] + b[i][j]);
            }
        }
    }

    file2 << dp[0][N - 1];
    file2.close();

    // FIX_ME: в main не было return 0 — для единообразия и предупреждений компилятора.
    // }
    return 0;
}
